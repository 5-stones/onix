export enum DateFormat {
  _00 = '00', // YYYYMMDD
  _01 = '01', // YYYYMM
  _02 = '02', // YYYYWW
  _03 = '03', // YYYYQ
  _04 = '04', // YYYYS
  _05 = '05', // YYYY
  _06 = '06', // YYYYMMDDYYYYMMDD
  _07 = '07', // YYYYMMYYYYMM
  _08 = '08', // YYYYWWYYYYWW
  _09 = '09', // YYYYQYYYYQ
  _10 = '10', // YYYYSYYYYS
  _11 = '11', // YYYYYYYY
  _12 = '12', // Text string
  _13 = '13', // YYYYMMDDThhmm
  _14 = '14', // YYYYMMDDThhmmss
  _20 = '20', // YYYYMMDD (H)
  _21 = '21', // YYYYMM (H)
  _25 = '25', // YYYY (H)
  _32 = '32', // Text string (H)
}